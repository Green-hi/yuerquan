package com.greenhi.lalababy.item;

public class ItemDataTools {

    private int iconID;
    private String title;

    public ItemDataTools(int iconID, String title) {
        this.iconID = iconID;
        this.title = title;
    }

    public int getIconID() {
        return iconID;
    }

    public String getTitle() {
        return title;
    }
}
